fun main() {
    //ao declarar uma variavel com val ocorre um erro na aplicação pois val é imutavel
    //o erro é Val cannot be reassigned
    val nome = "Luis"
    println("Ola  "+nome)
    nome = "Felipe"
    println("Ola  "+nome)
    nome = "Alex"
    println("Ola  "+nome)
    nome = "Gustavo"
    println("Ola  "+nome)
}